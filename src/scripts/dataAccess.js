const mainContainer = document.querySelector("#container")
//holds requests
//store external data when fetched in here
//start with empty requests array
const applicationState = {
requests: []
}

//puts information in requests array? did i fetch here
const API = "http://localhost:8088"
//fetch requests data from api
export const fetchRequests = () => {
    return fetch(`${API}/requests`)
        .then(response => response.json())
        .then(
            (serviceRequests) => {
                // Store the external state in application state
                applicationState.requests = serviceRequests
            }
        )
}

//returns copy of request state
export const getRequests = () => {
    return applicationState.requests.map(request => ({ ...request }))
}


//param from serviceform.js
export const sendRequest = (userServiceRequest) => {
    const fetchOptions = {
        method: "POST",
        headers: {
            "Content-Type": "application/json"
        },
        body: JSON.stringify(userServiceRequest)
    }


    return fetch(`${API}/requests`, fetchOptions)
        .then(response => response.json())
        .then(() => {
            mainContainer.dispatchEvent(new CustomEvent("stateChanged"))
        })
}

//deletes


export const deleteRequest = (id) => {
    return fetch(`${API}/requests/${id}`, { method: "DELETE" })
        .then(
            () => {
                mainContainer.dispatchEvent(new CustomEvent("stateChanged"))
            }
        )
}

